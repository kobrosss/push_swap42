#include "libft.h"

int	ft_isalpha(int c)
{
	int	i;

	i = 0;
	if (c >= 65 && c <= 90)
	{
		i++;
	}
	else if (c >= 97 && c <= 122)
	{
		i++;
	}
	if (i == 1)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
