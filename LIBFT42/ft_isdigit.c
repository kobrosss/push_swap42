#include "libft.h"

int	ft_isdigit(int c)
{
	int	i;

	i = 0;
	if (c >= 48 && c <= 57)
	{
		i++;
	}
	if (i == 1)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
